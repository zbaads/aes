void keyScheduleCore(byte *temp, int rconi){
	int i;	//generic iterator

	//rotate the bytes one to the left
	byte t = temp[0];
	temp[0] = temp[1];
	temp[1] = temp[2];
	temp[2] = temp[3];
	temp[3] = t;

	//S-Box all four bytes
	for(i = 0;i < 4;i++){
		temp[i] = sbox[temp[i]];
	}

	//xor rcon with the leftmost byte
	temp[0] = temp[0] ^ rcon[rconi];
}

// assert( cipherkey is keyn bytes long and expandedkey is keyb )
void keyExpansion(byte *cipherkey, byte *expandedkey)
{
	int i;	//generic iterator
	int x;  //another generic iterator for nesting
	int y;	//generic math variable
	int rconi = 1;	//initial rcon iteration value
	int bytecount = 0;
	byte temp[4];

	//copy the first keyn bytes of cipherkey to expanded key
	while(bytecount < keyn){
		expandedkey[bytecount] = cipherkey[bytecount];
		bytecount++;
	}

	bytecount--;

	while(bytecount < keyb){

		//assign previous 4 bytes of expanded key to temp
		for(i=0;i < 4;i++){
			temp[i] = expandedkey[bytecount - (3 - i)];
		}

		//perform keyScheduleCore on temp with rconi as the rcon iteration value
		keyScheduleCore(temp, rconi);

		//increment the rcon iteration value
		rconi++;

		for(x = 0;x < 4;x++){
			//xor temp with the four-block byte that is keyn bytes before the to-be-added values in the expanded key
			for(i = 0;i < 4;i++){
				temp[i] = temp[i] ^ expandedkey[((bytecount + 1) - keyn) + i];
			}

			//add temp to the end of keyn and update bytecount
			for(i = 0; i < 4;i++){
				expandedkey[++bytecount] = temp[i];
			}
		}

		//only run for a 256-bit key
		if(bitkey == 256){
			//S-Box the bytes in temp, then do the xor assign routine
			for(i = 0;i < 4;i++){
				temp[i] = sbox[temp[i]];
			}

			for(i = 0;i < 4;i++){
				temp[i] = temp[i] ^ expandedkey[((bytecount + 1) - keyn) + i];
			}

			for(i = 0; i < 4;i++){
				expandedkey[++bytecount] = temp[i];
			}
		}

		//if 192-bit key xor assign twice, if 256 do it three times
		if(bitkey == 128) y = 0;
		if(bitkey == 192) y = 2;
		if(bitkey == 256) y = 3;

		for(x = 0; x < y;x++){

			for(i = 0;i < 4;i++){
				temp[i] = temp[i] ^ expandedkey[((bytecount + 1) - keyn) + i];
			}

			for(i = 0; i < 4;i++){
				expandedkey[++bytecount] = temp[i];
			}
		}
	}
}

void subBytes(byte *state)
{
	int i;	//generic iterator

	for(i = 0;i < 16;i++){
			state[i] = sbox[state[i]];
	}
}

void shiftRows(byte *state)
{
	byte t;	//byte temp for rotation

	//rotate "row 2"
	t = state[4];
	state[4] = state[5];
	state[5] = state[6];
	state[6] = state[7];
	state[7] = t;

	//rotate "row 3"
	t = state[8];
	state[8] = state[10];
	state[10] = t;
	t = state[9];
	state[9] = state[11];
	state[11] = t;

	//rotate "row 4"
	t = state[15];
	state[15] = state[14];
	state[14] = state[13];
	state[13] = state[12];
	state[12] = t;
}

