#include <stdio.h>
#include "aes.h"

int main(int argc, char *argv[])
{
	byte shortkey[16] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f};

	byte expandedkey[176];

	keyExpansion(shortkey, expandedkey);

	byte state[16] = {0xdb, 0xf2, 0x01, 0xc6, 0x13, 0x0a, 0x01, 0xc6, 0x53, 0x22, 0x01, 0xc6, 0x45, 0x5c, 0x01, 0xc6};

	mixColumns(state);

	int i;
	for(i = 0;i < 16;i++){
		printf(" %02x ", state[i]);
		if((i + 1) % 4 == 0){
			printf("\n");
		}
	}

	return 0;
}
